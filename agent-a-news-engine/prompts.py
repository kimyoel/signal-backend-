# ──────────────────────────────────────────
SCORING_PROMPT = """
다음 뉴스 제목과 내용을 보고 투자 관련 중요도를 1~5 숫자 하나만 반환하라.

중요도 기준:
5 = 시장에 즉각적 영향 (중앙은행 금리 결정, 암호화폐 30%+ 급변, 전쟁 발발 등)
4 = 주목할 뉴스 (Fed 의장 발언, BTC 10%+ 변동, 대형 규제 발표)
3 = 일반 경제/투자 뉴스 (기업 실적, 경제지표 발표)
2 = 낮은 관련성 (간접적 경제 뉴스)
1 = 무관하거나 광고성

숫자 하나만 반환. 설명 금지.

뉴스 제목: {title}
뉴스 내용 (앞부분): {content_preview}
"""

